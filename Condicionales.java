public class Condicionales {
    public static void main(String[] args) {
        // Condicionales
        // Son aquellas variables
        // que pueden tener 2 posibles valores
        // true o false (Verdadero o falso)

        int edad = 17;
        if (edad >= 18){
            System.out.println("La persona es mayor de edad");
        } else {
            System.out.println("La persona es menor de edad");
        }

        // tenga la ine actualizada
        boolean ineActualizada = false;
        if (ineActualizada){
            System.out.println("Podrá votar este próximo 2 de junio");
        } else {
            System.out.println("No podrá votar el 2 de junio");
        }


    }
}
